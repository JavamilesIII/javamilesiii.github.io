        <h2 class="article-heading">ePortfolio</h2>
        <h3 class="article-heading">Prinzip/Ziel</h3>
        <p>Für einen ePortfolio sammelt man Informationen, entweder vom Unterricht oder von dem vorhandenen Arbeitsmaterial. Mit den gesammelten Informationen erstellt man einen Beitrag auf einer Website und veröffentlicht den zusammengefassten Inhalt des Themas.</p>
        <h3 class="article-heading">Was ist das?</h3>
        <p>Ein ePortfolio ist so eine digitale Sammelmappe, die uns ein Leben lang begleitet, um diese Wissenssammlung wiederzuverwenden. Sie wachst mit uns mit (inhaltlich) und ist meistens nur persönlich, allenfalls teil öffentlich zugänglich.</p>
        <h3 class="article-heading">Wichtig!</h3>
        <p><strong>Notizen </strong>sind <strong>keine Portfolios</strong>, denn sie haben keine Reflexion. <strong>Dateiablagen </strong>sind ebenfalls <strong>keine Portfolios</strong>, denn bei einer Dateiablage werden alle Dateien, sowohl Wichtige als auch Unwichtige, abgelegt. Im Gegensatz werden beim Portfolio einzelne wichtige Dateien ausgewählt. Diese Auswahl muss nicht nur von Erfolge sein, denn aus Fehlern lernt man.</p>
