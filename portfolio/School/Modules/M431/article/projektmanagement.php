    <h1><a href="#">Projektmanagement</a></h1>
    <div class="article-box">
        <p>Projektmanagement ist eigentlich selbsterklärend. Es ist die Organisation und Leitung von Projekten, um Ziele innerhalb von Zeit, Budget und Ressourcen zu erreichen.</p>
        <p>Projektmanagement&nbsp;findet man überall, egal ob bei einer Party oder bei einem Bau von einem Wolkenkratzer.</p>
    </div>
    <div class="article-box">
        <h2 class="article-heading">Projekte</h2>
        <p>Nicht alles ist ein Projekt und deswegen gibt es einige Kriterien, die beachtet werden müssen:</p>
        <h3 class="article-heading">Definiertes Ziel</h3>
        <p>Ein Projekt kann nicht nur Musik hören sein. Ein Projekt hat ein bestimmtes Ziel, wie z.B. für eine Geburtstagsparty ist das Ziel, seinen Geburtstag mit Anderen zu feiern.</p>
        <h3 class="article-heading">Neuartigkeit</h3>
        <p>Projekte sind keine Sachen, die man schon mehrmals gemacht hat. Ein Projekt ist etwas Neues zu machen oder zu produzieren.</p>
        <h3 class="article-heading">Einmaligkeit</h3>
        <p>Dieses Kriterium ist ähnlich wie Neuartigkeit, denn ein Projekt ist nicht immer das Gleiche und muss speziell sein, das womöglich nur einmal in einem Leben vorkommen kann.</p>
        <h3 class="article-heading">Gewisse Komplexität</h3>
        <p>Ein Projekt startet man von Neuem und hat nicht gerade alle Lösungen und Antworten zu Problemen und Fragen vorhanden.</p>
        <h3 class="article-heading">Begrenzte Ressourcen</h3>
        <p>Ein Projekt heisst nicht „einen Baum klettern“ oder so, sondern dafür werden begrenzte Ressourcen gebraucht, bspw. Seil zum Klettern oder Geld, um Materialien zu kaufen.</p>
        <h3 class="article-heading">Unsicherheiten</h3>
        <p>Natürlich kann ein Projekt nicht ohne ein Problem erfüllt werden. Bspw. bei einer Programmentwicklung gibt es keine Garantie, dass es ohne Bugs durchgeführt wird.</p>
    </div>
    <div class="article-box">
        <h2 class="article-heading">Entscheiden</h2>
        <p>Mit der Durchführung von Kreativitätstechniken werden sehr viele Ideen und Lösungsvarianten kreiert. Demnächst werden die Vorschläge präsentiert und es wird der geeignete Lösungsweg ausgewählt. Dazu muss man die Machbarkeit prüfen und beachten, dass noch übergeordnete Gründe, wie Gegengeschäfte, den Preis, die Erfahrung, die Technologie etc., den Entscheid beeinflussen.</p>
        <p>Zu beachten ist, was das Projekt zum Scheitern bringen kann. Es sind Probleme, wie:</p>
        <ul class="article-list">
            <li>Fehlende Unterstützung durch das Top-Management</li>
            <li>Schwache Projektmanager</li>
            <li>Fehlendes Wissen oder fehlende Fertigkeiten der Teammitglieder</li>
            <li>Fachexperten nicht ausreichend für das Projekt verfügbar</li>
            <li>Anforderungen und Erfolgskriterien nicht richtig festgelegt</li>
            <li>Kein Prozess zum Management von Änderungen des Umfangs</li>
            <li>Nicht effektiver Umgang mit Terminplanung</li>
            <li>Wichtige Ressourcen werden für höher-priorisierte Projekte abgezogen</li>
            <li>Kein Business-Case für das Projekt</li>
        </ul>
    </div>
    <div class="article-box">
        <h2 class="article-heading">Projektauslöser</h2>
        <p>Natürlich kommen die Projekte nicht vom Nichts. Es gibt zwei Arten von Auslösern: innen und aussen.</p>
        <h3 class="article-heading">Innen:</h3>
        <p>Projektauslöser von Innen entstehen aus Bedürfnissen, Zielen oder Herausforderungen innerhalb der Organisation selbst.</p>
        <p>Beispiele:</p>
        <ul class="article-list">
            <li>Die Kommunikation verbessern, um noch effizienter und produktiver zu sein</li>
            <li>Eine neue Idee von Projekt, die von der Organisation kommt, um es auf dem Markt zu verkaufen.</li>
            <li>Die Verbesserung aller kosten belastenden, nicht funktionierenden Prozesse innerhalb der Organisation.</li>
        </ul>
        <h3 class="article-heading">Aussen:</h3>
        <p>Projektauslöser von Aussen sind Einflüsse oder Anforderungen, die von ausserhalb der Organisation kommen, wie z.B. von Kunden, Lieferanten oder gesetzlichen Vorschriften.</p>
        <p>Beispiele:</p>
        <ul class="article-list">
            <li>Ein Kunde bestellt ein mass geschnittenes Produkt, das seinen Anforderungen entspricht.</li>
            <li>Eine gesetzliche Änderung erfordert die Umsetzung neuer Richtlinien, die in einer Branche eingehalten werden müssen.</li>
            <li>Der Lieferant hat neue Regeln für die Art und Weise, wie Dinge geliefert werden sollen, gemacht, also muss die Art zu arbeiten geändert werden.</li>
        </ul>
    </div>
    <div class="article-box">
        <h2 class="article-heading">Arbeitspakete</h2>
        <p>Projekte verlaufen nicht einfach so, sondern sie werden in Arbeitspacken unterteilt.</p>
        <p>Arbeitspakete sind kleinere, überschaubare Einheiten von Arbeit. Sie sind spezifisch genug, um klar verstanden und abgeschlossen zu werden, aber gross genug, um einen sinnvollen Beitrag zum Projektziel zu leisten. Ein Arbeitspaket beinhaltet Aufgaben, die von jemandem oder einer Gruppe bearbeitet werden können. Arbeitspakete helfen, den Übersicht über die Aufgaben und deren Reihenfolge zu halten.</p>
        <p>Es gibt zwei Arten von Strukturmodellen bei den Arbeitspaketen: Objektorientierter Projektstrukturplan</p>
        <p>Strukturiert Projekte durch Objekte mit spezifischen Eigenschaften und Verhalten. Funktionsorientierter Projektstrukturplan</p>
        <p>Organisiert Projekte nach funktionalen Aufgaben und Aktivitäten.</p>
    </div>
    <div class="article-box">
        <h2 class="article-heading">Rollen in Projekten</h2>
        <p>Die Arbeit in den Projekten macht nicht nur eine Person, sondern mehrere. Dazu gibt es verschiedene Rollen, die zu einem Projektmanagement formen.</p>
        <h3 class="article-heading">Antragsteller:</h3>
        <p>Die Person oder Organisation, die das Projekt initiiert und den Antrag stellt, um Ressourcen und Unterstützung für das Projekt zu erhalten.</p>
        <h3 class="article-heading">Bewilligungsgremium:</h3>
        <p>Eine Gruppe von Personen, die über die Genehmigung des Projektantrags und die Bereitstellung von Ressourcen entscheidet.</p>
        <h3 class="article-heading">Auftraggeber:</h3>
        <p>Die Person oder Organisation, die das Projekt finanziert und für die Umsetzung und Erfüllung der Projektziele verantwortlich ist. Profitiert auch am meisten vom Projekt.</p>
        <h3 class="article-heading">Entscheidungsgremium:</h3>
        <p>Eine Gruppe von Personen, die über wichtige Entscheidungen im Verlauf des Projekts, wie z.B. Änderungen im Projektumfang oder Budget, entscheidet.</p>
        <h3 class="article-heading">Projektleiter:</h3>
        <p>Die Person, die für die Gesamtorganisation, Planung und Leitung des Projekts verantwortlich ist. Der Projektleiter koordiniert die Arbeit des Teams und überwacht den Projektfortschritt.</p>
        <h3 class="article-heading">Projektmitarbeiter:</h3>
        <p>Personen, die aktiv an der Umsetzung des Projekts beteiligt sind und bestimmte Aufgaben oder Arbeitspakete übernehmen.</p>
        <h3 class="article-heading">Fachbeauftragte / Spezialisten:</h3>
        <p>Personen mit spezifischem Fachwissen oder Fähigkeiten, die für bestimmte Aufgaben im Projekt hinzugezogen werden, um ihre Expertise einzubringen und zum Erfolg des Projekts beizutragen.</p>
    </div>
    <div class="article-box">
        <h2 class="article-heading">Reflexion</h2>
        <p>Projektmanagement braucht man, wie gesagt, überall, wo es ein Projekt gibt. Es ist sehr inhaltlich und hat so viele Sachen, die man nicht unbedingt beachtet. Ich meine, nicht jede Person kennt die Charakteristiken eines Projekts. Es hat aber den meisten meiner Klasse etwas gebracht.</p>
