    <div class="article-box">
        <h2 class="article-heading">Was ist das?</h2>
        <p>IPERKA ist eine Methode, die ein Projekt in 6 Arbeitsschritte unterteilt. Die 6 Arbeitsschritte sind:</p>
        <h3 class="article-heading">Informieren</h3>
        <ul class="article-list">
            <li>Was will der Arbeitgeber?</li>
            <li>Was sind die Vorgaben?</li>
            <li>Was ist das Ziel?</li>
            <li>Welche Kenntnisse werden gebraucht?</li>
        </ul>
        <h3 class="article-heading">Planen</h3>
        <ul class="article-list">
            <li>Das Projekt wird geplant</li>
            <li>Verantwortungsbereiche werden aufgeteilt</li>
            <li>Varianten und Plan B werden überlegt</li>
            <li>Eventuelle Probleme ausfindig machen</li>
        </ul>
        <h3 class="article-heading">Entscheiden</h3>
        <ul class="article-list">
            <li>Entscheidung einer Lösung</li>
            <li>Kriterien für Qualität festlegen</li>
            <li>Verteilung der Arbeiten</li>
            <li>Klären, ob Idee nachvollziehbar</li>
        </ul>
        <h3 class="article-heading">Realisieren</h3>
        <ul class="article-list">
            <li>Start der Umsetzung</li>
            <li>Nach Plan arbeiten</li>
            <li>Vorgehen versichern</li>
        <li>Klären, ob Plan B einsatzbereit</li>
        </ul>
        <h3 class="article-heading">Kontrollieren</h3>
        <ul class="article-list">
            <li>Nach Vorgehens plan arbeiten</li>
            <li>Zeitplan und Ablauf im Auge behalten</li>
            <li>Kontrolle, ob alle Personen vorankommen</li>
            <li>Schauen, ob gut gearbeitet wird</li>
        </ul>
        <h3 class="article-heading">Auswerten</h3>
        <ul class="article-list">
            <li>Zurückschauen</li>
            <li>Gab es Probleme?</li>
            <li>Was war erfolgreich?</li>
            <li>Was muss verbessert werden?</li>
        </ul>
        <p>Es gibt mehrere Methoden, um diese Punkte durchzuführen. Jedoch eignet sich beim Planen Mindmapping. Es ist übersichtlicher und ist einfach zu verstehen.</p>
